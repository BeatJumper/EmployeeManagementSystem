package model;

public class action {

    public String A_ID;
    public String A_Name;
    public action(String a_ID, String a_Name) {
        A_ID = a_ID;
        A_Name = a_Name;
    }

    public String getA_ID() {
        return A_ID;
    }

    public void setA_ID(String a_ID) {
        A_ID = a_ID;
    }

    public String getA_Name() {
        return A_Name;
    }

    public void setA_Name(String a_Name) {
        A_Name = a_Name;
    }

}
